# Utilize um loop while e um loop for para contar de 0 até o número que o usuário digitar:

# ------------------------------------------ ESCREVA SEU CÓDIGO ABAIXO -----------------------------------------------------------

# LOOP WHILE
cont = 0
ate_num = int(input('Digite o numero desejado a ser contado: '))

while cont <= ate_num:
    print(cont)
    cont += 1





# LOOP FOR

cont = 0
at_num = int(input('Digite um numero inteiro: '))

for cont in range(at_num):
    print(cont)
    cont += 1

